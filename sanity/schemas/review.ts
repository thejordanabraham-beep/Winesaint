export const review = {
  name: 'review',
  title: 'Review',
  type: 'document',
  fields: [
    {
      name: 'wine',
      title: 'Wine',
      type: 'reference',
      to: [{ type: 'wine' }],
      validation: (Rule: any) => Rule.required(),
    },
    {
      name: 'score',
      title: 'Score',
      type: 'number',
      validation: (Rule: any) => Rule.required().min(1).max(10),
      description: 'Score from 1-10',
    },
    {
      name: 'shortSummary',
      title: 'Short Summary',
      type: 'string',
      description: 'One sentence summary (max 20 words)',
    },
    {
      name: 'tastingNotes',
      title: 'Tasting Notes / Full Review',
      type: 'text',
      rows: 6,
      validation: (Rule: any) => Rule.required(),
    },
    {
      name: 'flavorProfile',
      title: 'Flavor Profile',
      type: 'array',
      of: [{ type: 'string' }],
      description: '4-6 flavor descriptors',
    },
    {
      name: 'drinkThisIf',
      title: 'Drink This If',
      type: 'string',
      description: 'One sentence describing ideal buyer/occasion',
    },
    {
      name: 'foodPairings',
      title: 'Food Pairings',
      type: 'array',
      of: [{ type: 'string' }],
      description: '3-4 food pairing suggestions',
    },
    {
      name: 'drinkingWindowStart',
      title: 'Drinking Window Start',
      type: 'number',
      description: 'Year to start drinking',
    },
    {
      name: 'drinkingWindowEnd',
      title: 'Drinking Window End',
      type: 'number',
      description: 'Year to stop drinking',
    },
    {
      name: 'reviewerName',
      title: 'Reviewer Name',
      type: 'string',
      validation: (Rule: any) => Rule.required(),
    },
    {
      name: 'reviewDate',
      title: 'Review Date',
      type: 'date',
      validation: (Rule: any) => Rule.required(),
    },
    {
      name: 'isFeatured',
      title: 'Featured Review',
      type: 'boolean',
      initialValue: false,
    },
    {
      name: 'isAiGenerated',
      title: 'AI Generated',
      type: 'boolean',
      initialValue: false,
      description: 'Whether this review was generated by AI',
    },
    {
      name: 'scoreJustification',
      title: 'Score Justification',
      type: 'text',
      rows: 3,
      description: 'Explanation of how the score was calculated',
    },
    {
      name: 'criticAvg',
      title: 'Critic Average Used',
      type: 'number',
      description: 'The critic average score used for calculation',
    },
    {
      name: 'vivinoScore',
      title: 'Vivino Score Used',
      type: 'number',
      description: 'The Vivino score used for calculation',
    },
  ],
  preview: {
    select: {
      wine: 'wine.name',
      vintage: 'wine.vintage',
      score: 'score',
      reviewer: 'reviewerName',
      isAi: 'isAiGenerated',
    },
    prepare({ wine, vintage, score, reviewer, isAi }: any) {
      return {
        title: `${wine} ${vintage || ''} - ${score}pts`,
        subtitle: `by ${reviewer}${isAi ? ' (AI)' : ''}`,
      };
    },
  },
};
